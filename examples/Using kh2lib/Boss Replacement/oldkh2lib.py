    def read_csv(self, fn):
        d = {}
        with open(fn) as f:
            reader = csv.reader(f)
            i = 1
            for line in reader:
                if line[0] == 'Location Code':
                    continue
                d[i] = {"world": line[0], "room": line[1], "event": line[2], "numenemies": line[3], "loc": line[4], "obj": line[5], "name": line[6], "ttype": line[7], "msn": line[9]}
                i += 1
        return d
    def replace_boss(self, s, d):
        source = self.table[s]
        dest = self.table[d]
        obj = dest["obj"]
        if len(obj) == 6:
            alt = "01"
            dest["obj"] = obj[2:]
        else:
            alt = "00"
        replace_str = "Replacing {} with {}".format(source["name"], dest["name"])
        print(replace_str)
        self.apply_boss_code(source, dest, alt=alt, replace_str=replace_str)
        if self.change_location_code:
            self.apply_loc_code(source)
    def write_pnach(self, debug=False):
        with open(self.out_fn, "w") as f:
            for l in self.pnach:
                outstr = "{}\n".format(l)
                f.write(outstr)
                if debug:
                    print(outstr)
