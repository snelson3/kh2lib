import csv, os
class CodeGen:
    def __init__(self, fn="bosstable.csv", out_fn=os.path.join("cheats","F266B00B.pnach"), change_location_code=True):
        self.table = self.read_csv(fn)
        self.pnach = []
        self.out_fn=out_fn
        self.toHex = lambda b: hex(b)[2:].zfill(2).upper()
        self.change_location_code = change_location_code
    def apply_cb_code(self,code_str, comment=False):
        if comment:
            self.pnach.append("// {}".format(comment))
        for line in code_str.split("\n"):
            if len(line) == 0:
                continue
            line = line.strip().split()
            self.pnach.append("patch=1,EE,{},extended,{}".format(line[0],line[1]))
    def apply_ram_codes(self, codes):
        comment = None
        for code in codes.split("\n"):
            if len(code) == 0:
                continue
            if code[0] == "/":
                comment = code
                continue
            else:
                self.apply_cb_code(code, comment)
                comment = None
    def apply_boss_code(self, original, new, alt="00", replace_str=""):
        loc = original["loc"]
        obj = new["obj"]
        world = original["world"]
        room = original["room"]
        event = original["event"]
        num_e_3 = self.toHex(int(original["numenemies"],16)+3)
        num_e_2 = self.toHex(int(original["numenemies"],16)+2)
        num_e_1 = self.toHex(int(original["numenemies"],16)+1)
        num_e = original["numenemies"]
        # All the placement code does is make sure every replacement code isn't run every frame, which would choke the emulator
        # For a boss tester script, where there is only 1 repalcement code at a time, it should be fine to live without it
        # self.apply_cb_code(PLACEMENT_CODES.format(room,world,event,num_e_3,num_e_2,num_e_1,num_e))
        self.apply_cb_code("{0} 00{2}{1}".format(loc,obj,alt),replace_str)
        loc2 = self.toHex(int(loc, 16)+32)
        self.apply_cb_code("{0} 000000{1}".format(loc2,alt))
    def apply_loc_code(self, location):
        world = location["world"]
        room = location["room"]
        event = location["event"]
        world_code = WORLDS[str(world)]
        if world == "06b":
            world = "06"
        printstr = "Hold R2 During transition to spawn at {} fight in {}".format(location["name"], world_code[0])
        print(printstr)
        self.apply_cb_code(world_code[1].format(room,event), printstr)
